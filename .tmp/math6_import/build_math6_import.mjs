function task(topic, prompt, answer, wrong, difficulty, explanation, hint1, hint2 = "") {
  const choices = [answer, ...wrong].filter((x, i, a) => x && a.indexOf(x) === i);
  for (const fallback of ["0", "1", "2", "3", "4", "5", "10", "100"]) {
    if (choices.length >= 4) break;
    if (!choices.includes(fallback)) choices.push(fallback);
  }
  if (choices.length < 4) throw new Error(`Недостаточно уникальных вариантов: ${topic} / ${prompt}`);
  const shift = serial++ % 4;
  const options = choices.map((_, i) => choices[(i - shift + 4) % 4]);
  const correct = "abcd"[options.indexOf(answer)];
  rows.push([6, "Математика", topic, prompt, ...options, "", "", correct, difficulty, explanation, hint1, hint2]);
  topicCounts.set(topic, (topicCounts.get(topic) || 0) + 1);
}

